/*
 *  Constructs a KReloadNetworkDlg which is a child of 'parent', with the
 *  name 'name'.' 
 */
KReloadNetworkDlg::KReloadNetworkDlg( QWidget* parent,  const char* name )
    : KDialog( parent, name ),
      image0( (const char **) img0_kreloadnetworkdlg ),
      image1( (const char **) img1_kreloadnetworkdlg )
{
    if ( !name )
	setName( "KReloadNetworkDlg" );
    setMinimumSize( QSize( 342, 130 ) );
    setMaximumSize( QSize( 342, 130 ) );
    setIcon( image0 );

    tlNetworkStatus = new QLabel( this, "tlNetworkStatus" );
    tlNetworkStatus->setGeometry( QRect( 40, 72, 273, 20 ) );
    QFont tlNetworkStatus_font(  tlNetworkStatus->font() );
    tlNetworkStatus_font.setBold( TRUE );
    tlNetworkStatus->setFont( tlNetworkStatus_font ); 
    tlNetworkStatus->setAlignment( int( QLabel::AlignCenter ) );

    kpbOk = new KPushButton( this, "kpbOk" );
    kpbOk->setEnabled( FALSE );
    kpbOk->setGeometry( QRect( 139, 96, 58, 31 ) );
    kpbOk->setAutoResize( TRUE );

    PixmapLabel1 = new QLabel( this, "PixmapLabel1" );
    PixmapLabel1->setGeometry( QRect( 20, 20, 40, 40 ) );
    PixmapLabel1->setPixmap( image1 );
    PixmapLabel1->setScaledContents( TRUE );

    TextLabel1 = new QLabel( this, "TextLabel1" );
    TextLabel1->setGeometry( QRect( 73, 20, 291, 46 ) );
    languageChange();
    resize( QSize(342, 130).expandedTo(minimumSizeHint()) );
    clearWState( WState_Polished );

    // signals and slots connections
    connect( kpbOk, SIGNAL( clicked() ), this, SLOT( close() ) );
}

/*
 *  Destroys the object and frees any allocated resources
 */
KReloadNetworkDlg::~KReloadNetworkDlg()
{
    // no need to delete child widgets, Qt does it all for us
}

/*
 *  Sets the strings of the subwidgets using the current
 *  language.
 */
void KReloadNetworkDlg::languageChange()
{
    setCaption( tr2i18n( "Reloading Network" ) );
    tlNetworkStatus->setText( QString::null );
    kpbOk->setText( tr2i18n( "&OK" ) );
    TextLabel1->setText( tr2i18n( "Please wait while the network is reloaded so\n"
"the changes can take effect." ) );
}

